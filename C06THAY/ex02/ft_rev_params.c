#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
}

int	main(int argc, char *argv[])
{
	int	r;

	r = argc - 1 ;
	while (r > 0)
	{
		ft_putstr(argv[r]);
		write(1, "\n", 1);
		r--;
	}
}
